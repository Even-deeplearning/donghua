clc; clear all; close all;
I = imread('./images/test.png');
g = rgb2gray(I);
figure;
subplot(1, 2, 1); imshow(I, []); title('原图像');
subplot(1, 2, 2); imshow(g, []); title('灰度图像');
% 这段代码的功能是读取一张图片，将其转换为灰度图像，并在窗口中显示原图像和灰度图像。
% 具体的实现步骤如下：
% 首先，使用clc、clear all和close all命令，分别清除命令行窗口的内容、清除工作空间中的所有变量和关闭所有打开的图形窗口。
% 使用imread函数读取一张图片，图片的路径是'./images/test.png'。读取的结果存储在变量I中。
% 使用rgb2gray函数将彩色图像I转换为灰度图像，转换后的结果存储在变量g中。
% 创建一个窗口，并使用subplot函数在窗口中创建一个1行2列的图像布局。接下来的操作都是在这个窗口中进行。
% 使用imshow函数显示原图像I，[]表示将图像的数据范围调整到合适的范围。在subplot中，将这个图像显示在第1个子图中。
% 使用imshow函数显示灰度图像g，[]表示将图像的数据范围调整到合适的范围。在subplot中，将这个图像显示在第2个子图中。
% 使用title函数给每个子图添加标题，分别为'原图像'和'灰度图像'。
% 这段代码的目的是展示原图像和灰度图像的对比，方便用户观察和分析图像的特征。